#include "libft.h"

int	ft_atoi(const char *str)
{
	int	sign;
	int	nb;

	nb = 0;
	sign = 1;
	while ((*str >= 9 && *str <= 13) || *str == 32)
		str++;
	if (*str == '-' || *str == '+')
	{
		if (*str == '-')
			sign = -1;
		str++;
	}
	while (*str >= '0' && *str <= '9')
		nb = (nb * 10) + *str++ - 48;
	return (nb * sign);
}
